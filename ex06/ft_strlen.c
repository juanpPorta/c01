#include<unistd.h>
#include<stdio.h>

int	ft_strlen(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		str++;
		i++;
	}
	return (i);
}

int main()
{ 
    int i;
    
	i = ft_strlen("pepepepe");
    printf("%d", i);
}
